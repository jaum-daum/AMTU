<h3 id="recarga">5.1 RECARGA do CARTÃO</h3>
<p>Para poder recargar o cartão no sistema precisa seguir os seguintes passo:<br>
1-No menu inicial localize o botão de recarga(Parte Superior), Click em "Recarregar".<br>
    2-Aberto o Menu de usuario, click em "Recarregar".<br>
    3-Em tela de Recarregar, Preencher as informações pedida.<br>
    4-Apos preenchimento, Click em pagar.<br>
    4-Apos o pagamento, Click em "voltar ao inicio".<br>
    5-Visualizar Mensagem de "Pagamento efetuado com Sucesso".<br>
    5.1 caso não consiga, click no botão de ajuda
    5.2-Refazer passo a passo.
    <h3 id="consulta">5.4 CONSULTA DE SALDO</h3>
    <P>Para poder saber o quanto de saldo ainda resta no seu cartão , você precisa seguir os seguintes passo-a-passo:<br>
    1-No menu inicial localize o botão de consulta de saldo(Parte Superior), Click em "consultar saldo".<br>
    2-Aberto o MENU  de saldo, click em "VER SALDO".<br>
    3-Nessa tela você conseguirá Visualizar o saldo do seu cartão.<br>
    4-Caso queira Recarregar click no botão "Recarregar CARTÃO".<br>
    5-caso não consiga, click no botão de ajuda<br>
    5.1-Refazer passo a passo de "recarga de saldo".<br>
    <h3 id="bloqueio">5.3 BLOQUEIO DO CARTÃO</h3>
    <P>O bloqueio de cartão consiste em bloquear se cartão em caso de perda, siga o passo-a-passo:<br>
    1-No menu inicial localize o botão de Bloqueio de cartão(Parte Superior), Click em "bloquear".<br>
    2- Apos aberto o MENU  de bloqueio.<br>
    3- Será pedido seu cpf, e tera a opcão de cartão comun,cartão de Estudante.<br>
    4- apos a sua escolha você vizualizará a Mensagem"cartão bloqueado".<br>
    5- caso não consiga, volte para a pagina principal e click em Ajuda.<br>
    5- refaça o passo-a-passo.<br>
    <h3 id="disque">5.4 DISQUE DENÚNCIA</h3>
    <P> Para utilizar o serviço disque-denuncia siga o passo-a-passo<br>
    1-No menu inicial localize o botão de Fazer denúncia(Parte Superior).<br>
    2- Apos aberto a pagina de "BLOQUEIO".<br>
    3- tera uma barra de escrita para voce colocar sua denuncia.<br>
    4- logo apos click em "enviar".<br>
    4-E então analizaremos sua denuncia e tomaremos as devidas ações.<br>


            

