    <h3>3.1 Cadastro de Motorista</h3>
    1-No menu inicial localize o Cabeçalho(Parte Superior), Click em "motorista".<br>
    2-Aberto o Menu de Estudante, click em "Cadastrar".<br>
    3-Em tela de Cadastro, Preencher as informações pedida.<br>
    4-Apos preenchimento, Click em Cadastrar.<br>
    5-Visualizar Mensagem de "Cadastro Realizado com Sucesso".<br>
    5.1-Caso alguma infromação preenchida incorreto, Mensagem "Erro ao Cadastrar".<br>
    5.2-Refazer passo a passo.
    <p>O cadastro de motorista, consiste em ajudar motoristas que procuram um emprego como motorista, na area publica ou privada.possibilitando que motoristas com um bom histórico no trânsito tenham direito a diversos benefícios e até mesmo descontos. </p>
